# -*- coding: utf-8 -*-


DEFAULT_PREVIEW_TEMPLATE = """
ПОСТАНОВЛЕНИЕ
об оплате труда адвоката

{дата постановления}

{вводная часть}

УСТАНОВИЛ:

В производстве суда находилось уголовное дело № {номер дела} в отношении {подсудимый рп ио}.

Защиту интересов {подсудимый рп ио} осуществлял адвокат {адвокат ип ио}.

Адвокатом заявлена выплата вознаграждения в сумме {заявлено адвокатом} рублей.

Согласно представленным сведениям адвокат участвовал в судебных заседаниях:
{заседания}.

{услуги адвоката}

Размер вознаграждения адвоката за один день участия составляет {вознаграждение} рублей,
с учётом {+ 20% (районный коэффициент)} {+ 30% (непрерывный стаж работы)} —
{вознаграждение с процентами} рублей.

Всего адвокатом оказано услуг: {количество услуг}.
Сумма вознаграждения за услуги составляет {вознаграждение за услуги} рублей
({вознаграждение за услуги прописью}).

{дополнительные постановления}

{взыскание или освобождение}

На основании изложенного, руководствуясь ст. 131, 132 УПК РФ,

ПОСТАНОВИЛ:

{взыскать или освободить}

{под стражей}

Судья: {судья ио}
"""


class PreviewRenderer(object):
    def __init__(self, context_builder, tag_resolver):
        self.context_builder = context_builder
        self.tag_resolver = tag_resolver

    def render(self, state):
        context = self.context_builder.build(state)
        return self.tag_resolver.render(DEFAULT_PREVIEW_TEMPLATE, context)